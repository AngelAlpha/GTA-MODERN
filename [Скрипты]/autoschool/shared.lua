addEventHandler("onClientVehicleEnter",getRootElement(),
    function (player,seat)
    	if player ~= localPlayer then return end
    	if seat ~= 0 then return end
    	if getElementData(localPlayer, "VehSchool") == source then return end
    	if getElementData(source, "OwnerSchool") then
    		if getElementData(source, "OwnerSchool") ~= player then
    			setControlState(player, "enter_exit", true )
    			outputChatBox("[#FF0022Автошкола#ffffff] Это транспортное средство не Ваше!",255,255,255,true)
    			return
    		end
    	end
    	for _,src in pairs(id_Lic) do
    		if getElementModel(source) == src[1] then
    			if getElementData(player, src[2]) ~= true and seat == 0 then
    				outputChatBox("[#FF0022Автошкола#ffffff] У Вас нет лицензии для управления этим транспортом!",255,255,255,true)
    				setTimer(function()
    					setControlState(player, "enter_exit", true )
    				end,50,10)
    			end
    		end
    	end
    end
)
